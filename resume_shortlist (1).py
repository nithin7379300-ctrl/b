def extract_text_from_pdf(filepath):
    """Extract clean text from a PDF file using pdfplumber."""
    text = ""
    with pdfplumber.open(filepath) as pdf:
        for page in pdf.pages:
            page_text = page.extract_text()
            if page_text:
                text += page_text + " "
    text = re.sub(r'\s+', ' ', text).strip()
    return text

