# Cell 7: Model 2 - spaCy NLP Semantic Similarity + Skill Matching

# Common tech skill keywords to look for
SKILL_KEYWORDS = [
    "python", "java", "sql", "machine learning", "deep learning", "nlp",
    "tensorflow", "pytorch", "keras", "scikit-learn", "pandas", "numpy",
    "data analysis", "statistics", "computer vision", "aws", "gcp", "azure",
    "docker", "git", "tableau", "power bi", "r", "spark", "hadoop",
    "data visualization", "neural network", "regression", "classification"
]

def extract_skills(text):
    """Extract matched skill keywords from text."""
    text_lower = text.lower()
    return set(skill for skill in SKILL_KEYWORDS if skill in text_lower)

def spacy_score(jd_text, resume_text):
    """Compute spaCy semantic similarity + skill overlap score."""
    # Semantic similarity via spaCy word vectors
    jd_doc     = nlp(jd_text[:10000])   # spaCy has token limit
    res_doc    = nlp(resume_text[:10000])
    semantic   = jd_doc.similarity(res_doc)

    # Skill overlap score
    jd_skills  = extract_skills(jd_text)
    res_skills = extract_skills(resume_text)
    if len(jd_skills) == 0:
        skill_score = 0
    else:
        matched     = jd_skills & res_skills
        skill_score = len(matched) / len(jd_skills)

    # Weighted combination: 50% semantic + 50% skill match
    final = (0.5 * semantic) + (0.5 * skill_score)
    return round(final * 100, 2), round(semantic * 100, 2), round(skill_score * 100, 2)

print("Running spaCy NLP scoring...\n")
spacy_data = []
for name, text in resumes.items():
    final, semantic, skill = spacy_score(job_description, text)
    jd_skills  = extract_skills(job_description)
    res_skills = extract_skills(text)
    matched    = jd_skills & res_skills
    missing    = jd_skills - res_skills
    spacy_data.append({
        'Candidate'       : name,
        'NLP_Score'       : final,
        'Semantic_Score'  : semantic,
        'Skill_Score'     : skill,
        'Skills_Matched'  : len(matched),
        'Skills_Missing'  : len(missing),
        'Matched_Skills'  : ', '.join(sorted(matched)) or 'None',
        'Missing_Skills'  : ', '.join(sorted(missing)) or 'None'
    })

spacy_results = pd.DataFrame(spacy_data).sort_values(
    'NLP_Score', ascending=False).reset_index(drop=True)
